<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      The practice of using a network of remote servers hosted the internet to store, manage and process data (rather than on local server / local data centre):
    </p>
    <ol>
      <li>
        Compute power
      </li>
      <li>
        Storage
      </li>
      <li>
        Networking
      </li>
      <li>
        Database space
      </li>
    </ol>
    <p>
      
    </p>
    <p>
      Benefits:
    </p>
    <ul>
      <li>
        Highly Available Flexible ,Cost-Effective (Scalability)
      </li>
      <li>
        Reliable and Predictable
      </li>
      <li>
        Secure, Easy to Manage (Governance)
      </li>
      <li>
        Easily and Highly Configurable (Manageability)
      </li>
    </ul>
